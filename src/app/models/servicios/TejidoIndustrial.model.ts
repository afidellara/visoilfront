export class TejidoIndustrial {

    id: String
    imagen: String
    color: String
    texto: String
    tipo: String

    constructor(
        id: String,
        imagen: String,
        color: String,
        texto: String,
        tipo: String
    ) {
        this.id = id
        this.imagen = imagen
        this.color = color
        this.texto = texto
        this.tipo = tipo
    }
}